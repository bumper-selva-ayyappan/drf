    def test_filter_by_tags(self):
        """Test filtering recipes by tags"""
        r1 = create_recipe(user=self.user, title='Chicken Fried Rice')
        r2 = create_recipe(user=self.user, title='Veg Friend Rice')
        r3 = create_recipe(user=self.user, title='Pongal')

        tag1 = Tag.objects.create(user=self.user, name='NV')
        tag2 = Tag.objects.create(user=self.user, name='Veg')

        r1.tags.add(tag1)
        r2.tags.add(tag2)

        params = {'tags': f'{tag1.id},{tag2.id}'}
        res = self.client.get(RECIPES_URL, params)

        s1 = RecipeSerializer(r1)
        s2 = RecipeSerializer(r2)
        s3 = RecipeSerializer(r3)
        self.assertIn(s1.data, res.data)
        self.assertIn(s2.data, res.data)
        self.assertNotIn(s3.data, res.data)

